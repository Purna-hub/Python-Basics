#creating a dictionary with Integer keys

dict1 = {1:'radha',2:'sita',3:'sulthana',4:'suresh'}

#creating dictionary with mixed keys
dict2 = {1:"sno","name":'raja','address':'hyderabad',2:[1,2,3,4]}

print(dict1)

print(dict2)

#reading the elements of the dictionary using keys

print(dict1[1])
print(dict2[2])

#reading the keys available in dictionaries
print(dict1.keys())
print(dict2.keys())

#reading the values of dictionaries using values() function
print(dict1.values())
print(dict2.values())




